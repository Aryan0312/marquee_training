package SwitchCase;

public class SwitchCase {

    public static void main(String[] args) {
        // a switch case is used to select one block of code from multiple opitons
        // instead of writing if else ladders we use switch 
        // swtch can only use int , string, char , byte


        // enum

        // static and final 

        
        int day = 2;
        switch(day){
            case 1:
                System.out.println("Tuesday");
                break;

            case 2:
                System.out.println("Tuesday");
                break;

            default:
                System.out.println("default");
        }
    }
}
